""" Ejercicios de conversiones de tipos """

# Ejercicio 1
entero = int(input("Ingrese un numero entero: "))
flotante = float(entero)
print(f"Resultado: {flotante}")

# Ejercicio 2
num1 = int(input("Ingrese un primer numero entero: "))
num2 = int(input("ingrese un segundo numero entero: "))
print(f"Suma: {num1 + num2}")
print(f"Resta: {num1 - num2}")
print(f"Multiplicacion: {num1 * num2}")
print(f"Division: {num1 / num2}")

# Ejercicio 3
num_entero = int(input("Ingrese un numero entero: "))
num_flotante = float(input("Ingrese un numero flotante: "))
print(f"La suma es: {num_entero + num_flotante}")

# Ejercicio 4
horas = float(input("Horas trabajadas: "))
costo = float(input("Costo por hora: "))
print(f"Paga resultante: {horas * costo}")

# Ejercicio 5
edad = int(input("Ingrese su edad: "))
